public class Viewer {

    /**Método que dá print a uma string
     *
     * @param a String
     */
    public static void prints(String a){
        System.out.println(a);
    }

    /**Método que dá print a um inteiro
     *
     * @param a Integer
     */
    public static void prints(Integer a){ System.out.println(a); }

    /**Método que dá print a um double
     *
     * @param a Double
     */
    public static void prints(Double a){ System.out.println(a); }

    /**Método que dá print a um topUsers
     *
     * @param top topUsers
     */
    public static void prints(topUsers top){
        System.out.println(top);
    }

}
